create or replace view v_quote_reconciliation as
select
  qh.quote_id,
  qh.customer_id,
  qh.country_code,
  qh.policy_type,
  qh.coverage_amount,
  qh.customer_segment,
  qh.payment_frequency,
  qh.consent_status,
  qh.eligibility_outcome,
  qh.pricing_baseline_version,
  qh.base_premium,
  qh.risk_loading_amount,
  qh.risk_factor_applied,
  qh.discount_amount,
  qh.tax_amount,
  qh.tax_rate_applied,
  qh.country_adjustment_amount,
  qh.country_adjustment_source,
  qh.final_premium,
  qh.processing_status,
  qh.quote_outcome_status,
  uc.workflow_status as underwriting_status,
  qc.age,
  qc.eligibility_rule_version,
  qc.pricing_rule_version,
  qc.tax_rule_version,
  qc.country_adjustment_rule_version,
  array_agg(distinct ae.event_type) filter (where ae.event_type is not null) as audit_outcomes,
  array_agg(distinct ce.business_outcome) filter (where ce.business_outcome is not null) as consent_outcomes
from quote_header qh
left join quote_context qc on qc.quote_id = qh.quote_id
left join underwriting_case uc on uc.quote_id = qh.quote_id
left join audit_event ae on ae.quote_id = qh.quote_id
left join consent_event ce on ce.quote_id = qh.quote_id
group by
  qh.quote_id,
  qh.customer_id,
  qh.country_code,
  qh.policy_type,
  qh.coverage_amount,
  qh.customer_segment,
  qh.payment_frequency,
  qh.consent_status,
  qh.eligibility_outcome,
  qh.pricing_baseline_version,
  qh.base_premium,
  qh.risk_loading_amount,
  qh.risk_factor_applied,
  qh.discount_amount,
  qh.tax_amount,
  qh.tax_rate_applied,
  qh.country_adjustment_amount,
  qh.country_adjustment_source,
  qh.final_premium,
  qh.processing_status,
  qh.quote_outcome_status,
  uc.workflow_status,
  qc.age,
  qc.eligibility_rule_version,
  qc.pricing_rule_version,
  qc.tax_rule_version,
  qc.country_adjustment_rule_version;
